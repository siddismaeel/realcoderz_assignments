package com;

public class Department {
	private int depId;
	private String detName;
	
	public Department(int depId, String detName) {
		super();
		this.depId = depId;
		this.detName = detName;
	}

	public int getDepId() {
		return depId;
	}

	public String getDetName() {
		return detName;
	}
	
	

}
